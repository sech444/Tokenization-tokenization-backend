# Function to build application
build_application() {
    print_status "Building Rust application..."

    # Clean build cache if needed
    if [ "$1" = "--clean" ]; then
        cargo clean
    fi

    # Build the application
    cargo build
    print_success "Application built successfully"
}

# Function to check system resources
check_system_resources() {
    print_status "Checking system resources..."

    # Check available memory
    local mem_total=$(grep MemTotal /proc/meminfo | awk '{print int($2/1024)}')
    local mem_available=$(grep MemAvailable /proc/meminfo | awk '{print int($2/1024)}')

    print_info "Memory: ${mem_available}MB available / ${mem_total}MB total"

    if [ "$mem_available" -lt 1024 ]; then
        print_warning "Low available memory. Consider closing other applications."
    fi

    # Check disk space
    local disk_available=$(df . | awk 'NR==2 {print int($4/1024)}')
    print_info "Disk space: ${disk_available}MB available"

    if [ "$disk_available" -lt 2048 ]; then
        print_warning "Low disk space. Consider freeing up space."
    fi
}

