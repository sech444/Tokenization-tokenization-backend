# Function to print status
print_status() {
    echo -e "${BLUE}[$(date +'%H:%M:%S')]${NC} $1"
}

print_success() {
    echo -e "${GREEN}✅ $1${NC}"
}

print_warning() {
    echo -e "${YELLOW}⚠️  $1${NC}"
}

print_error() {
    echo -e "${RED}❌ $1${NC}"
}

print_info() {
    echo -e "${CYAN}ℹ️  $1${NC}"
}

# Function to check if command exists
command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# Function to check if PostgreSQL is running
check_postgres() {
    if pg_isready -h "$DB_HOST" -p "$DB_PORT" >/dev/null 2>&1; then
        return 0
    else
        return 1
    fi
}

# Function to start PostgreSQL service
start_postgres() {
    print_status "Starting PostgreSQL service..."

    if check_postgres; then
        print_success "PostgreSQL is already running"
        return 0
    fi

    # Try different ways to start PostgreSQL
    if command_exists systemctl; then
        sudo systemctl start postgresql
        sudo systemctl enable postgresql
    elif command_exists service; then
        sudo service postgresql start
    else
        print_error "Cannot start PostgreSQL automatically. Please start it manually."
        return 1
    fi

    # Wait for PostgreSQL to be ready
    local attempts=0
    while [ $attempts -lt 30 ]; do
        if check_postgres; then
            print_success "PostgreSQL is now running"
            return 0
        fi
        attempts=$((attempts + 1))
        sleep 1
    done

    print_error "PostgreSQL failed to start within 30 seconds"
    return 1
}

# Function to setup database
setup_database() {
    print_status "Setting up databases..."

    # Get PostgreSQL version and user info
    local pg_version=$(psql --version | awk '{print $3}' | cut -d'.' -f1,2)
    print_info "PostgreSQL version: $pg_version"

    # Create main database
    print_status "Creating database: $DB_NAME"
    if sudo -u postgres createdb "$DB_NAME" 2>/dev/null; then
        print_success "Database '$DB_NAME' created"
    else
        print_info "Database '$DB_NAME' might already exist"
    fi

    # Create test database
    print_status "Creating test database: $DB_TEST_NAME"
    if sudo -u postgres createdb "$DB_TEST_NAME" 2>/dev/null; then
        print_success "Test database '$DB_TEST_NAME' created"
    else
        print_info "Test database '$DB_TEST_NAME' might already exist"
    fi

    # Set up database user permissions
    print_status "Setting up database permissions..."
    sudo -u postgres psql -c "ALTER USER postgres PASSWORD 'postgres123';" 2>/dev/null || true

    print_success "Database setup completed"
}

# Function to install Rust tools
install_rust_tools() {
    print_status "Installing required Rust tools..."

    # Install sqlx-cli
    if cargo install --list | grep -q "sqlx-cli"; then
        print_success "sqlx-cli already installed"
    else
        print_status "Installing sqlx-cli..."
        cargo install sqlx-cli --no-default-features --features postgres
        print_success "sqlx-cli installed"
    fi

    # Install cargo-watch (optional but useful)
    if cargo install --list | grep -q "cargo-watch"; then
        print_success "cargo-watch already installed"
    else
        print_status "Installing cargo-watch..."
        cargo install cargo-watch
        print_success "cargo-watch installed"
    fi
}

