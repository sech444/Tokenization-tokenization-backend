# Function to run health check
health_check() {
    print_status "Running system health check..."

    # Check PostgreSQL
    if check_postgres; then
        print_success "PostgreSQL is accessible"
    else
        print_error "PostgreSQL is not accessible"
        return 1
    fi

    # Check database exists
    if PGPASSWORD=postgres123 psql -h "$DB_HOST" -U postgres -lqt | cut -d \| -f 1 | grep -qw "$DB_NAME"; then
        print_success "Database '$DB_NAME' exists"
    else
        print_error "Database '$DB_NAME' not found"
        return 1
    fi

    # Check if we can build
    if cargo check >/dev/null 2>&1; then
        print_success "Rust code compiles successfully"
    else
        print_error "Rust compilation issues found"
        return 1
    fi

    print_success "Health check passed!"
}

